package ch02_datatypes;

public class D_03_Program_to_Display_Floating_Point_Data_Types {

	public static void main(String[] args) {
		// Print Floating datatypes

		float fl = 100f; // float Size: 4 bytes
		System.out.print("Float value is :");
		System.out.println(fl);

		double dou = -99; // double Size: 8 bytes
		System.out.print("Double value is :");
		System.out.println(dou);
	}
}
