package ch03_operators;

public class O_01_Arithmetic_Operators {

	public static void main(String[] args) {
		// 1.Addition of Two Numbers
		int a = 8;
		int b = 9;
		int sum;

		// logic sum=a+b
		sum = a + b;
		System.out.print("Sum of two numbers : ");
		System.out.println(sum);

		// 2.Subtraction of Two Numbers
		int a1 = 10;
		int b1 = 9;
		int sub;

		// logic sub=a1-b1
		sub = a1 - b1;
		System.out.print("Subtraction of two numbers : ");
		System.out.println(sub);

		// 3.Multiplication of Two Numbers
		int a2 = 10;
		int b2 = 9;
		int mul;

		// logic mul=a+b
		mul = a2 * b2;
		System.out.print("Multiplication of two numbers : ");
		System.out.println(mul);

		// 4.Division of Two Numbers
		int a3 = 10;
		int b3 = 9;
		int div;

		// logic div=a3/b3
		div = a3 / b3;
		System.out.print("Division of two numbers : ");
		System.out.println(div);

		// 5.Find Remainder Using Modulus Operator
		int a4 = 29;
		int b4 = 9;
		int rem;

		// logic rem=a4%b4
		rem = a4 % b4;
		System.out.print("Remainder after division of two numbers : ");
		System.out.println(rem);
	}
}
