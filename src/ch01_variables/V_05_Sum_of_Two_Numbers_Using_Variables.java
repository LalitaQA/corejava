package ch01_variables;

public class V_05_Sum_of_Two_Numbers_Using_Variables {

	public static void main(String[] args) {
		// Take two variables and sum their values.

		int a = 3;
		int b = 8;
		int sum;
		// logic a+b
		sum = a + b;
		System.out.print("Sum of a+b: ");
		System.out.println(sum);
	}
}
